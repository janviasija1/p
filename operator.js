//arithmetic operators
let a = 10;
let b = 3;
console.log("Addition: ", a + b);         
console.log("Subtraction: ", a - b);      
console.log("Multiplication: ", a * b);   
console.log("Division: ", a / b);         
console.log("Modulus: ", a % b);          

//assignment operators
let x = 5;
console.log("Initial x:", x);
x += 2;  
console.log("x += 2:", x);
x -= 1;
console.log("x -= 1:", x);
x *= 3;
console.log("x *= 3:", x);
x /= 2;
console.log("x /= 2:", x);
x %= 2;
console.log("x %= 2:", x);

//comparison operators 
let m = 10, n = 20;
console.log("m == n:", m == n);         
console.log("m != n:", m != n);         
console.log("m === n:", m === n);       
console.log("m !== n:", m !== n);       
console.log("m > n:", m > n);           
console.log("m < n:", m < n);           
console.log("m >= 10:", m >= 10);       
console.log("n <= 20:", n <= 20);       

//logical operator
let p = true, q = false;
console.log("p && q:", p && q);         
console.log("p || q:", p || q);         
console.log("!p:", !p);                 

//ternary oprerator
let age = 18;
let result = (age >= 18) ? "Eligible" : "Not Eligible";
console.log("Ternary Result:", result);





